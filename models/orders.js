class orders{
    constructor(id, title, price, userId, address, phone, quantity, name, date){
        this.id = id;
        this.title = title;
        this.price = price;
        this.userId = userId;
        this.address = address;
        this.phone = phone;
        this.quantity = quantity;
        this.name = name;
        this.date = date;
    }
}

export default orders;